/* Global helpers — available to both homepage.js and booking.js */
let lang = localStorage.getItem('atx_lang') || 'en';
function t(key) { return (I18N[lang] || {})[key] || (I18N.en || {})[key] || key; }
function setLang(next) { lang = next; localStorage.setItem('atx_lang', lang); }

/* Generic data-attribute walker — used by pages that don't have a bespoke
 * applyLang() in their own script (pros.html, packages.html, legal pages,
 * 404). Supports three patterns:
 *
 *   <h1 data-i18n="key">…</h1>           textContent = t(key)
 *   <p data-i18n-html="key">…</p>         innerHTML   = t(key)   // for HTML
 *   <input data-i18n-attr="placeholder:key">   sets attribute(s)
 *   <input data-i18n-attr="placeholder:k1;aria-label:k2">         multi
 */
function applyI18n(root = document) {
  if (root === document) document.documentElement.lang = lang;
  root.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  root.querySelectorAll('[data-i18n-attr]').forEach(el => {
    el.dataset.i18nAttr.split(';').forEach(pair => {
      const [attr, key] = pair.split(':').map(s => s && s.trim());
      if (attr && key) el.setAttribute(attr, t(key));
    });
  });
}

/* Standard EN/ES toggle button hook — call from any page-script that wants
 * the default behavior (toggle, persist, repaint button, re-walk i18n).
 * Returns the button element or null if missing. */
function bindLangToggle(onChange) {
  const btn = document.getElementById('langToggle');
  if (!btn) return null;
  const paint = () => {
    btn.innerHTML =
      `<span class="lang-flag ${lang === 'en' ? '' : 'off'}">EN</span> / ` +
      `<span class="lang-flag ${lang === 'es' ? '' : 'off'}">ES</span>`;
  };
  btn.addEventListener('click', () => {
    setLang(lang === 'en' ? 'es' : 'en');
    paint();
    if (typeof onChange === 'function') onChange();
  });
  paint();
  return btn;
}
